    private void preencherTabela(List<com.mycompany.biblioteca.Autor> autores){
        DefaultTableModel model = new DefaultTableModel();
        TabelaAutores.setModel(model);
        
        model.addColumn("id");
        model.addColumn("Nome");
        model.addColumn("Genero");
        TabelaAutores.getColumnModel().getColumn(0).setPreferredWidth(10);
        
        for (com.mycompany.biblioteca.Autor autor : autores){
            model.addRow(new Object[]{ autor.getId(), autor.getNome(), autor.isGenero()==false?"Feminino":"Masculino"});
        }
        
        
        
    } 

    public void habilitarCriacao(boolean estadoCriar) {
        txtNomeAutor.setEnabled(estadoCriar);
        rbMasculino.setEnabled(estadoCriar);
        rbFeminino.setEnabled(estadoCriar);
        btnSalvar.setEnabled(estadoCriar);
        btnCriar.setEnabled(!estadoCriar);
        btnEditar.setEnabled(estadoCriar);
    }
    
    public void limparDados(){
        txtNomeAutor.setText("");
        rbMasculino.setSelected(true);
        
    }
