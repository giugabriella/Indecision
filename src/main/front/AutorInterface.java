    private void TabelaAutoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelaAutoresMouseClicked
        // TODO add your handling code here:
        btnCriar.setEnabled(false);
        btnEditar.setEnabled(true);
        btnApagar.setEnabled(true);
        int id = (int) TabelaAutores.getValueAt(TabelaAutores.getSelectedRow(), 0);
        autor = basedeDados.buscarAutor(id);
       String nome = autor.getNome();
       if(autor.isGenero()){
           rbMasculino.setSelected(true);
       }
       else{
           rbFeminino.setSelected(true);
       }
       TabelaAutores.setEnabled(false);
       txtNomeAutor.setText(nome);
    }//GEN-LAST:event_TabelaAutoresMouseClicked

